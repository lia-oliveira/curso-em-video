# int, float, bool, str


# Tipo int
# <class 'int'>
idade=25
print(type(idade))


# Tipo Float
# <class 'float'>
altura=1.85
print(type(altura))

# Tipo: bool
# <class 'bool'>
ehAlta=True
print(type(ehAlta))

# Tipo String
# <class 'str'>
nome='Maria'
print(type(nome))


