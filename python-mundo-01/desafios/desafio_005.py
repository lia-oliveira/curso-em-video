# Faça um programa que leia um número inteiro e mostre na tela o seu sucessor e seu antecessor.

n_int = int(input('Digite um número inteiro: '))

antecessor = n_int - 1
sucessor = n_int + 1

print('Número:', n_int)
print('Antecessor:', antecessor)
print('Sucessor:', sucessor)